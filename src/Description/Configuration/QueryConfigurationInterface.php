namespace AppserverIo\Ldap\Description\Configuration;

/**
 * Interface for LDAP repository query description implementations.
 *
 * @author    Tim Wagner <tw@appserver.io>
 * @copyright 2015 TechDivision GmbH <info@appserver.io>
 * @license   http://opensource.org/licenses/osl-3.0.php Open Software License (OSL 3.0)
 * @link      https://github.com/appserver-io/ldap
 * @link      http://www.appserver.io
 */
interface QueryConfigurationInterface extends ConfigurationInterface
{

    /**
     * Returns the query's search base.
     *
     * @return string The search base
     */
    public function getSearchBase();

    /**
     * Returns the query's filter.
     *
     * @return string The filter
     */
    public function getFilter();
}
